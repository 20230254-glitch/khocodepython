a = 16
b = 3
c = 5

# Số học
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a ** b)

# So sánh
print(a > b)
print(a < b)
print(a == b)
print(a != b)

# Gán
x = a
x += b
print(x)

x = a
x *= b
print(x)

x = a
x /= b
print(x)

# Logic
print((a > b) and (b > c))
print((a > b) or (b < c))
print(not(a > b))

# Bit
print(a & b)
print(a | b)
print(a ^ b)
print(~a)
print(a << 3)
print(a >> 2)